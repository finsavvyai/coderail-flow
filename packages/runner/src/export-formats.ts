/**
 * Export format implementations and step formatting helpers.
 */

/** Get human-readable step title */
export function getStepTitle(step: any): string {
  const titles: Record<string, string> = {
    goto: 'Navigate to URL',
    click: 'Click Element',
    fill: 'Fill Input',
    waitFor: 'Wait for Element',
    screenshot: 'Take Screenshot',
    caption: 'Show Caption',
    pause: 'Pause',
    scroll: 'Scroll',
    highlight: 'Highlight Element',
    assertText: 'Assert Text',
    assertUrl: 'Assert URL',
    executeScript: 'Execute Script',
  };
  return titles[step.type] || step.type;
}

/** Get step details as text */
export function getStepDetails(step: any): string | null {
  const details: string[] = [];
  if (step.url) details.push(`**URL**: ${step.url}`);
  if (step.selector) details.push(`**Selector**: \`${step.selector}\``);
  if (step.text) details.push(`**Text**: ${step.text}`);
  if (step.value) details.push(`**Value**: ${step.value}`);
  if (step.timeout) details.push(`**Timeout**: ${step.timeout}ms`);
  if (step.ms) details.push(`**Duration**: ${step.ms}ms`);
  return details.length > 0 ? details.join('\n') : null;
}

/** Get step details as HTML */
export function getStepDetailsHtml(step: any): string {
  const details = getStepDetails(step);
  if (!details) return '';
  const html = details
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(
      /`(.*?)`/g,
      '<code style="background: #e5e7eb; padding: 2px 6px; border-radius: 3px;">$1</code>'
    )
    .replace(/\n/g, '<br>');
  return `<div class="step-details">${html}</div>`;
}

/** Export flow as Markdown documentation */
export function exportAsMarkdown(flow: any): string {
  const lines: string[] = [];
  lines.push(`# ${flow.name}`);
  lines.push('');
  if (flow.description) {
    lines.push(flow.description);
    lines.push('');
  }
  lines.push('## Flow Steps');
  lines.push('');

  const steps = flow.definition?.steps || [];
  steps.forEach((step: any, index: number) => {
    lines.push(`### Step ${index + 1}: ${getStepTitle(step)}`);
    lines.push('');
    if (step.narrate) {
      lines.push(`**Narration**: ${step.narrate}`);
      lines.push('');
    }
    const details = getStepDetails(step);
    if (details) {
      lines.push(details);
      lines.push('');
    }
  });

  lines.push('---');
  lines.push(`*Generated by CodeRail Flow*`);
  return lines.join('\n');
}

