/** Export flow as YAML */
export function exportAsYaml(flow: any): string {
  const lines: string[] = [];
  lines.push(`name: ${flow.name}`);
  if (flow.description) lines.push(`description: ${flow.description}`);
  lines.push('definition:');
  lines.push('  params:');

  const params = flow.definition?.params || [];
  if (params.length === 0) {
    lines.push('    []');
  } else {
    params.forEach((param: any) => {
      lines.push(`    - name: ${param.name}`);
      lines.push(`      type: ${param.type}`);
      lines.push(`      required: ${param.required}`);
    });
  }

  lines.push('  steps:');
  const steps = flow.definition?.steps || [];
  steps.forEach((step: any) => {
    lines.push(`    - type: ${step.type}`);
    Object.keys(step).forEach((key) => {
      if (key !== 'type' && key !== 'narrate') {
        const value = step[key];
        if (typeof value === 'string') lines.push(`      ${key}: "${value}"`);
        else if (typeof value === 'boolean') lines.push(`      ${key}: ${value}`);
        else if (typeof value === 'number') lines.push(`      ${key}: ${value}`);
      }
    });
  });

  return lines.join('\n');
}
